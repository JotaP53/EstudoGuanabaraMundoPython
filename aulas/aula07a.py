n1 = int(input('Primeiro valor: '))
n2 = int(input('Segundo valor: '))

sm = n1 + n2
sb = n1 - n2
mp = n1 * n2
exp = n1 ** n2
div = n1 / n2
divi = n1 // n2
rdiv = n1 % n2

print("Os valores são: sm {}, sb {}, mp {}".format(sm, sb, mp))
print("Os valores são: exp {}, div {}, divi {}, rdiv {}".format(exp, div, divi, rdiv))
